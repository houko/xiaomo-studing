package info.xiaomo.tree;

/**
 * 二叉树
 */
public class BinaryTree {
    /**
     * 根结点
     * 可空
     * 根为空时，叫空树。此时 左子树和左子树都为空
     */
    private TreeNode root;

    /**
     * 左子树
     * 可空
     */
    private TreeNode leftTree;

    /**
     * 右子树
     * 可空
     */
    private TreeNode rightTree;

}
